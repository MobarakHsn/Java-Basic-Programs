public class Mammal
{
	private boolean Hair;
	private boolean Fur;
	
	public void hasHair(boolean Hair)
	{
		this.Hair=Hair;
	}
	
	public boolean hasHair()
	{
		return Hair;
	}
	
	public void hasFur(boolean Fur)
	{
		this.Fur=Fur;
	}
	
	public boolean hasFur()
	{
		return Fur;
	}
}